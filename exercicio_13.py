dia = int(input('Digite um número '))

if dia == 1:
    print('Esse dia corresponde a Segunda-feira')

elif dia == 2:
    print('Esse dia corresponde a Terça-feira')

elif dia == 3:
    print('Esse dia corresponde a Quarta-feira')

elif dia == 4:
    print('Esse dia corresponde a Quinta-feira')

elif dia == 5:
    print('Esse dia corresponde a Sexta-feira')

elif dia == 6:
    print('Esse dia corresponde a Sábado-feira')

elif dia == 7:
    print('Esse dia corresponde a Domingo-feira')

else: 
    print('Esse número não corresponde a nenhum dia da semana')