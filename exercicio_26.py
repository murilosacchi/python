estacao = int(input('Digite o número referente a sua estação do ano preferida\n\n1 - Verão\n2 - Outono\n3 - Inverno\n4 - Primavera\n\n'))

if estacao == 1:
  print('\nIr à praia')

elif estacao == 2:
  print('\nFazer uma trilha')

elif estacao == 3:
  print('\nAssistir uma série')

elif estacao == 4:
  print('\nPassear no parque')

else:
  print('\nNúmero inválido')